def get_location_frame(location_name):
    """Get the frame for a specific location"""
    location_name = location_name.lower()
    if location_name in LOCATION_METADATA:
        return LOCATION_METADATA[location_name]['frame']
    return None

def get_location_element(location_name, element_name):
    """Get a specific element from a location"""
    location_name = location_name.lower()
    element_name = element_name.lower()
    if location_name in LOCATION_METADATA:
        elements = LOCATION_METADATA[location_name].get('elements', {})
        if element_name in elements:
            return elements[element_name]['ascii']
    return None

def get_location_colors(location_name):
    """Get color scheme for a location"""
    location_name = location_name.lower()
    if location_name in LOCATION_METADATA:
        return LOCATION_METADATA[location_name].get('colors', {})
    return None

def get_location_metadata(location_name):
    """Get full metadata for a location"""
    location_name = location_name.lower()
    return LOCATION_METADATA.get(location_name)

def render_location_scene(location_name, content=""):
    """Render a complete location scene with frame and content"""
    frame = get_location_frame(location_name)
    if not frame:
        return None

    # Replace placeholder with actual content
    return frame.replace('[SCENE CONTENT HERE]', content) if content else frame

def get_all_locations():
    """Get list of all available locations"""
    return list(LOCATION_METADATA.keys())

def get_location_elements(location_name):
    """Get all elements for a specific location"""
    location_name = location_name.lower()
    if location_name in LOCATION_METADATA:
        return LOCATION_METADATA[location_name].get('elements', {})
    return {}

def print_banner(text, width=60):
    """Print a simple text banner with borders"""
    border = "═" * width
    padding = " " * ((width - len(text) - 2) // 2)

    print(f"╔{border}╗")
    print(f"║{padding}{text}{padding}{'  ' if len(text) % 2 == 0 else ' '}║")
    print(f"╚{border}╝")

def create_health_bar(current, max_val, width=20):
    """Create an ASCII health/mana bar"""
    filled = int((current / max_val) * width)
    empty = width - filled

    bar = "█" * filled + "░" * empty
    percentage = int((current / max_val) * 100)

    if percentage > 66:
        color = "💚"  # Green
    elif percentage > 33:
        color = "💛"  # Yellow
    else:
        color = "💔"  # Red

    return f"{color} [{bar}] {current}/{max_val}"

